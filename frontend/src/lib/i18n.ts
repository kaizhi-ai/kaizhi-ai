import i18n from "i18next"
import { initReactI18next } from "react-i18next"

export const DEFAULT_LANGUAGE = "zh-CN"

export const languageOptions = [
  { value: "zh-CN", labelKey: "languages.zhCN" },
  { value: "en-US", labelKey: "languages.enUS" },
] as const

export type SupportedLanguage = (typeof languageOptions)[number]["value"]

const SUPPORTED_LANGUAGES = new Set<string>(
  languageOptions.map((option) => option.value)
)
const LANGUAGE_STORAGE_KEY = "kaizhi.language"

export function supportedLanguage(value?: string | null): SupportedLanguage {
  if (value && SUPPORTED_LANGUAGES.has(value)) return value as SupportedLanguage
  return DEFAULT_LANGUAGE
}

export function languageFromNavigator(): SupportedLanguage {
  const languages = navigator.languages?.length
    ? navigator.languages
    : [navigator.language]
  for (const language of languages) {
    const normalized = language.toLowerCase()
    if (normalized.startsWith("zh")) return "zh-CN"
    if (normalized.startsWith("en")) return "en-US"
  }
  return DEFAULT_LANGUAGE
}

export function getStoredLanguage(): SupportedLanguage {
  return supportedLanguage(localStorage.getItem(LANGUAGE_STORAGE_KEY))
}

export function setStoredLanguage(language: string) {
  localStorage.setItem(LANGUAGE_STORAGE_KEY, supportedLanguage(language))
}

export function initialLanguage(): SupportedLanguage {
  const stored = localStorage.getItem(LANGUAGE_STORAGE_KEY)
  return stored ? supportedLanguage(stored) : languageFromNavigator()
}

